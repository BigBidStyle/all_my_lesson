print("Урок № 11.4 - Задача № 2 практика - Герон.")
import math
a = float(input("Введите сторону а треугольника: "))
b = float(input("Введите сторону b треугольника: "))
c = float(input("Введите сторону с треугольника: "))
p = (a + b + c) / 2
s = round(math.sqrt(p * (p - a) * (p -b) * (p - c)), 2)
print("Площадь треугольника равна: ", s)
